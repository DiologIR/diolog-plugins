// to-stylex.mjs — turn a captured reference subtree into a React component DRAFT
// (improvement #7). Two modes, chosen by whether the section is editable:
//
//   --mode embed   pixel-EXACT: emits a component that renders the lifted HTML (inlined
//                  computed styles) verbatim. Use for DECORATIVE, non-CMS sections (a
//                  hero illustration, a stat panel) — fastest path to identical, and the
//                  literal "extract the HTML and render it in a React component" approach.
//   --mode stylex  emits a StyleX SKELETON: `stylex.create({...})` from the inlined
//                  styles + a JSX tree, mapping each value to a TOKEN name when a token
//                  map is supplied (--tokens tokens.json). Use for anything that must
//                  become a real, token-driven, editable component. The output is a
//                  reviewed DRAFT, not final — you clean up names + wire content.
//
//   node to-stylex.mjs --capture capture.hero.json --name HeroVignette --mode embed
//   node to-stylex.mjs --capture capture.hero.json --name HeroVignette --mode stylex --tokens tokens.json
//
// --tokens tokens.json is a flat { "<tokenName>": "<computed value>" } map (e.g. dumped
// from your StyleX defineVars / theme); a value that matches a token is emitted as the
// token reference and the import is collected, so the generated component uses tokens
// consistently and FLAGS values that have no token (a `/* TODO: no token */` comment).

import { readFileSync, writeFileSync } from 'node:fs';

const args = Object.fromEntries(
  process.argv.slice(2).reduce((a, v, i, arr) => {
    if (v.startsWith('--')) a.push([v.slice(2), arr[i + 1]?.startsWith('--') ? true : arr[i + 1]]);
    return a;
  }, []),
);
if (!args.capture || !args.name) {
  console.error('usage: node to-stylex.mjs --capture <capture.json> --name <ComponentName> [--mode embed|stylex] [--tokens tokens.json] [--out file]');
  process.exit(2);
}
const MODE = args.mode || 'stylex';
const NAME = args.name;
let cap = JSON.parse(readFileSync(args.capture, 'utf8'));
if (typeof cap === 'string') cap = JSON.parse(cap);
if (cap.error || (!cap.tree && !cap.html)) {
  console.error(`capture is empty or errored (${cap.error || 'no tree/html'}) — re-run capture-subtree.js with a MF_CAPTURE_SELECTOR that exists on the page (Framer exports use <div>, not <nav>/<header>).`);
  process.exit(2);
}
const tokens = args.tokens ? JSON.parse(readFileSync(args.tokens, 'utf8')) : null;

const camelToKebab = (s) => s.replace(/[A-Z]/g, (m) => '-' + m.toLowerCase());
const cssEscape = (s) => String(s).replace(/`/g, '\\`');

// ----- embed mode ---------------------------------------------------------
function emitEmbed() {
  const html = cap.html || '';
  return `import * as stylex from '@stylexjs/stylex';

/**
 * ${NAME} — pixel-exact embed of a captured reference subtree (decorative; inlined
 * computed styles). Generated by to-stylex.mjs --mode embed. For NON-editable sections
 * only. If this needs to become token-driven / CMS-editable, regenerate with --mode stylex.
 *
 * SECURITY: HTML below is BUILD-TIME-STATIC reference markup baked into the bundle — it
 * is NOT user input, so dangerouslySetInnerHTML is safe here. If you ever make this
 * content dynamic (props, CMS, user data), run it through a sanitizer (DOMPurify / the
 * repo's sanitize util) FIRST, or convert to real JSX (regenerate with --mode stylex).
 * Review the markup before shipping.
 */
const HTML = ${'`'}
${cssEscape(html)}
${'`'};

const s = stylex.create({ wrap: { display: 'contents' } });

export function ${NAME}(): React.JSX.Element {
  return <div {...stylex.props(s.wrap)} aria-hidden dangerouslySetInnerHTML={{ __html: HTML }} />;
}
`;
}

// ----- stylex mode --------------------------------------------------------
const usedTokens = new Set();
function tokenFor(value) {
  if (!tokens) return null;
  for (const [name, v] of Object.entries(tokens)) {
    if (String(v).toLowerCase() === String(value).toLowerCase()) { usedTokens.add(name); return name; }
  }
  return null;
}
function valueExpr(value) {
  const tok = tokenFor(value);
  if (tok) return `tokens.${tok}`;
  return JSON.stringify(value);
}

let styleId = 0;
const styleDefs = [];
function defStyle(style) {
  const id = `n${styleId++}`;
  const lines = Object.entries(style).map(([k, v]) => {
    const tok = tokenFor(v);
    return `    ${k}: ${tok ? `tokens.${tok}` : JSON.stringify(v)},${tok ? '' : ' /* TODO: no token */'}`;
  });
  styleDefs.push(`  ${id}: {\n${lines.join('\n')}\n  },`);
  return id;
}
function jsx(node, indent) {
  if (node.raw) return ' '.repeat(indent) + '{/* icon/image — port the SVG/asset */}\n' + ' '.repeat(indent) + node.raw;
  const id = Object.keys(node.style).length ? defStyle(node.style) : null;
  const tag = node.tag === 'p' || node.tag === 'span' || /^h[1-6]$/.test(node.tag) ? node.tag : 'div';
  const open = id ? `<${tag} {...stylex.props(s.${id})}>` : `<${tag}>`;
  if (!node.children.length) return ' '.repeat(indent) + open + (node.text || '') + `</${tag}>`;
  const kids = node.children.map((c) => jsx(c, indent + 2)).join('\n');
  const txt = node.text ? '\n' + ' '.repeat(indent + 2) + node.text : '';
  return ' '.repeat(indent) + open + '\n' + kids + txt + '\n' + ' '.repeat(indent) + `</${tag}>`;
}

function emitStylex() {
  const body = jsx(cap.tree, 2);
  const tokenImport = usedTokens.size
    ? `import { ${[...usedTokens].sort().join(', ')} } from '@/components/tokens.stylex'; // TODO: fix import path/names\n`
    : '';
  // tokens are referenced as `tokens.<name>` in defStyle; alias them here:
  const tokenAlias = usedTokens.size ? `const tokens = { ${[...usedTokens].map((t) => `${t}`).join(', ')} } as Record<string, string>;\n` : '';
  return `import * as stylex from '@stylexjs/stylex';
${tokenImport}
/**
 * ${NAME} — StyleX SKELETON generated from a captured reference subtree by
 * to-stylex.mjs --mode stylex. This is a DRAFT: review every value, replace the
 * \`/* TODO: no token *\/\` literals with real tokens, name the styles meaningfully,
 * and wire real content/props. Verify against the reference with the differ after.
 */
${tokenAlias}const s = stylex.create({
${styleDefs.join('\n')}
});

export function ${NAME}(): React.JSX.Element {
  return (
${body}
  );
}
`;
}

const out = MODE === 'embed' ? emitEmbed() : emitStylex();
const file = args.out || `${NAME}.tsx`;
writeFileSync(file, out);
console.log(`to-stylex (${MODE}) → ${file}${MODE === 'stylex' && !tokens ? '  (no --tokens: every value is a literal; pass a token map to auto-map)' : ''}`);
